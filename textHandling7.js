let country = "Nigeria";

// indexOf example
let result1 = country.indexOf("i");
console.log(`The position of "i" in ${country} is: ${result1}`); // 1

let result2 = country.indexOf("a");
console.log(`The position of "a" in ${country} is: ${result2}`); // 6

let result3 = country.indexOf("z");
console.log(`The position of "z" in ${country} is: ${result3} [not found]`); // -1 (not found)

// lastIndexOf example
let result4 = country.lastIndexOf("i");
console.log(`The last position of "i" in ${country} is: ${result4}`); // 5

let result5 = country.lastIndexOf("a");
console.log(`The last position of "a" in ${country} is: ${result5}`); // 6

let result6 = country.lastIndexOf("z");
console.log(`The last position of "z" in ${country} is: ${result6} [not found]`); // -1 (not found)




